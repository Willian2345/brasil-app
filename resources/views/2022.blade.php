O futebol é mesmo uma paixão nacional?
Sem dúvidas, esse é o esporte mais popular, porém está longe de ser uma unanimidade no Brasil:

30% dos respondentes declararam não gostar de futebol. Entre os que gostam, a maioria se enquadra no perfil “simpatizante” – aqueles que acompanham as partidas de forma esporádica, em momentos decisivos ou quando há uma grande competição.
Muito além de um evento esportivo
A Copa do Mundo é um evento que envolve grande sociabilidade e, por isso, atrai os olhares até mesmo daqueles que dizem não gostar de futebol. Este é o momento em que as pessoas querem se reunir com os amigos, com a família, ligar a TV e assistir aos jogos acompanhados de cerveja, petiscos e churrasco.



