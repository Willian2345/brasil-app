Ainda pela primeira fase, o Brasil jogou contra a Suécia em 28 de junho. Aos 24 minutos, Brolin fez um lançamento longo para Kennet Anderson, que matou no peito e deu um toque de classe: a bola encobriu Taffarel e entrou no canto esquerdo. No segundo tempo, Romário foi levando a bola desde a intermediária até a entrada da área. Bastou um bico na bola em direção ao canto esquerdo para marcar 1 x 1. O resultado deu ao Brasil o primeiro lugar do Grupo na classificação para as oitavas de final.

No dia do aniversário de sua independência, 4 de julho, o país anfitrião da Copa enfrentou o Brasil no Stanford Stadium. O lance mais polêmico do jogo aconteceu aos 43 minutos, junto à linha lateral, quando Leonardo deu uma cotovelada na têmpora de Tab Ramos, que foi ao chão com uma concussão craniana. A agressão levou à expulsão do lateral brasileiro. Aos 28 minutos do segundo tempo, Romário buscou a bola no meio do campo e avançou em direção ao gol americano. A três metros da linha da grande área, Bebeto recebeu a bola e acertou no canto direito de Meola. O abraço de Bebeto e Romário emocionou a torcida.

A partida entre Brasil e Holanda pelas quartas de final, em 9 de julho, balançou o Estádio Cotton Bowl, em Dallas. Foram cinco gols, todos no segundo tempo, numa das melhores atuações da Seleção Brasileira na Copa e boa exibição da Holanda. Aos sete minutos do tempo final, Romário pegou um cruzamento de Bebeto e, de primeira, chutou com o peito do pé sem chances para o goleiro Ed de Goey. Aos 17 minutos, Bebeto dominou a bola escapou de Wouters, driblou o goleiro e aumentou para 2 a 0. Aproveitando uma falha da defesa brasileira, Bergkamp tocou no ângulo esquerdo de Taffarel aos 19 minutos. Aos 31, Overmars cobrou um escanteio para Aron Winter, que empatou de cabeça. O jogo ficou difícil, mas Branco desempatou. Aos 36 minutos, acertou as redes com um chute de pé esquerdo, fechando o placar em 3 x 2.  

O sétimo confronto entre Brasil e Suécia em Copas do Mundo aconteceu nas semifinais, em 13 de julho, no Estádio Rose Bowl, em Pasadena. O Brasil fez diversas tentativas de marcar no primeiro tempo, mas a bola não entrava. Aos 35 minutos do segundo tempo, Jorginho cruzou para Romário, que aproveitou um erro de posicionamento da defesa sueca e, sozinho diante do gol, cabeceou no canto esquerdo e classificou o Brasil para a final da Copa do Mundo. 

Em 1994, o Brasil foi tetracampeão mundial de futebol com a seguinte equipe: Taffarel (goleiro), Jorginho (lateral), Aldair (zagueiro), Márcio Santos (zagueiro), Branco (lateral), Mauro Silva (meio-campo), Dunga (meio-campo), Mazinho (meio-campo), Zinho (meio-campo), Bebeto (atacante) e Romário (atacante). Reservas: Gilmar (goleiro), Zetti (goleiro), Cafu (lateral), Ricardo Rocha (zagueiro), Ronaldão (zagueiro), Leonardo (lateral), Raí (meio-campo), Paulo Sérgio (meio-campo), Müller (atacante), Ronaldinho (atacante) e Viola (atacante). Técnico: Carlos Alberto Parreira.




